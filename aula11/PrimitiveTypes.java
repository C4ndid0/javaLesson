package aula11;

public class PrimitiveTypes {
  public static void main(String[] args) {
    byte number01 = 8;
    short number02 = 16;
    int number04 = 32;
    long number05 = 64;

    System.out.println("number: " + number01);
    System.out.println("number: " + number02);
    System.out.println("number: " + number04);
    System.out.println("number: " + number05);
  }
}
